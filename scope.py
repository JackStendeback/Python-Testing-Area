# Local Scope is a variable that is only avaliable to its particular function or area.
# Global Scope is a variable that is available to everything in the file if used.
# There is NO Block scope in python. A variable defined inside a code block is available to its nearest function/global.
# Do not alter global scope inside a function unless no other option.
# Try returning the new value instead of altering it.
# For constant variables, use all uppercase when declaring a global variable. i.e: URL/PI
